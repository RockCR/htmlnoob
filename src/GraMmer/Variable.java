package GraMmer;

public class Variable {
    public static void main(String[] args) {
        byte b=10;System.out.println(b);
        short s=100;System.out.println(s);
        int i=1000;System.out.println(i);
        double d=13.14;System.out.println(d);
        char c='a';System.out.println(c);
        //定义boolean类型的变量
        //boolean b=true;System.out.println(b);
        //定义log类型的变量
        //log l=10000000l;System.out.println(l);
        float f= (float) 13.14;
        System.out.println(f);
    }
}
